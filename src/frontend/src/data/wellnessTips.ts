/**
 * Returns the wellness tip for today based on the day of the year (1–365).
 * Tip rotates every 24 hours and is the same for all users on a given calendar date.
 */
export function getTodaysTip(): string {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay); // 1–365
  return wellnessTips[
    (dayOfYear - 1 + wellnessTips.length) % wellnessTips.length
  ];
}
